const issues = (files) => analyzeNazareTheme(files).issues;
const hasIssue = (files, code) =>
	issues(files).some((issue) => issue.code === code);

test("metafield definition and read collection compose deterministically", () => {
	const snapshot = {
		path: ".shopify/metafields.json",
		contents: JSON.stringify({
			definitions: [{ owner: "product", namespace: "custom", key: "subtitle" }],
		}),
	};
	const accesses = [
		{
			id: "access:subtitle",
			fromPath: "sections/main.liquid",
			object: "product",
			propertyPath: "metafields.custom.subtitle",
			expression: "product.metafields.custom.subtitle",
			origin: "liquid",
		},
	];
	const definitions = collectMetafieldDefinitions(snapshot);
	const reads = joinMetafieldReads(
		definitions.definitions,
		collectMetafieldReads(accesses),
	);
	const analysis = analyzeMetafields(snapshot, accesses);
	assert.deepEqual(definitions.definitions, analysis.definitions);
	assert.deepEqual(reads, analysis.reads);
	assert.equal(reads[0].definitionId, "metafield:product:custom:subtitle");
});

test("theme analysis cache reuses unchanged files and invalidates edits", () => {
	const cache = { version: 1, entries: {} };
	const files = [
		{ path: "snippets/card.liquid", contents: "{{ product.title }}" },
		{ path: "locales/en.json", contents: '{"card":{"title":"Card"}}' },
	];
	const first = analyzeNazareTheme(files, { cache });
	const cachedSnippet = cache.entries["snippets/card.liquid"];
	const cachedLocale = cache.entries["locales/en.json"];
	assert.ok(cachedSnippet);
	assert.ok(cachedLocale);

	const second = analyzeNazareTheme(
		[
			files[0],
			{ path: "locales/en.json", contents: '{"card":{"title":"Tile"}}' },
		],
		{ cache },
	);
	assert.strictEqual(cache.entries["snippets/card.liquid"], cachedSnippet);
	assert.notStrictEqual(cache.entries["locales/en.json"], cachedLocale);
	assert.deepEqual(second.ir.variableReads, first.ir.variableReads);

	analyzeNazareTheme([files[0]], { cache });
	assert.equal(cache.entries["locales/en.json"], undefined);
});

test("theme input validation rejects invalid JSON shapes and unsafe paths", () => {
	for (const [path, contents, code] of [
		["config/settings_schema.json", "{}", "THEME_SETTINGS_SCHEMA_INVALID_ROOT"],
		["config/settings_data.json", "[]", "THEME_SETTINGS_DATA_INVALID_ROOT"],
		[
			"templates/index.json",
			'{"sections":[]}',
			"THEME_TEMPLATE_INVALID_SECTIONS",
		],
		["locales/en.json", "[]", "THEME_LOCALE_INVALID_ROOT"],
	]) {
		assert.equal(hasIssue([{ path, contents }], code), true, path);
	}

	for (const path of [
		"",
		".",
		"C:/theme/file.liquid",
		"a/./b.liquid",
		"a\u0000b.liquid",
	]) {
		assert.equal(
			hasIssue([{ path, contents: "" }], "THEME_UNSAFE_PATH"),
			true,
			JSON.stringify(path),
		);
	}
});

