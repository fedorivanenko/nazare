import assert from "node:assert/strict";
import test from "node:test";
import { analyzeNazareTheme, inspectNazareTheme } from "../dist/index.js";

const issues = (files) => analyzeNazareTheme(files).issues;
const hasIssue = (files, code) =>
	issues(files).some((issue) => issue.code === code);

test("theme input validation rejects invalid JSON shapes and unsafe paths", () => {
	for (const [path, contents, code] of [
		["config/settings_schema.json", "{}", "THEME_SETTINGS_SCHEMA_INVALID_ROOT"],
		["config/settings_data.json", "[]", "THEME_SETTINGS_DATA_INVALID_ROOT"],
		[
			"templates/index.json",
			'{"sections":[]}',
			"THEME_TEMPLATE_INVALID_SECTIONS",
		],
		["locales/en.json", "[]", "THEME_LOCALE_INVALID_ROOT"],
	]) {
		assert.equal(hasIssue([{ path, contents }], code), true, path);
	}

	for (const path of [
		"",
		".",
		"C:/theme/file.liquid",
		"a/./b.liquid",
		"a\u0000b.liquid",
	]) {
		assert.equal(
			hasIssue([{ path, contents: "" }], "THEME_UNSAFE_PATH"),
			true,
			JSON.stringify(path),
		);
	}
});

test("theme analysis accepts Shopify-generated JSON preambles", () => {
	const analysis = analyzeNazareTheme([
		{
			path: "templates/index.json",
			contents:
				"/*\n * IMPORTANT: generated by Shopify\n */\n" +
				JSON.stringify({ sections: { hero: { type: "hero" } } }),
		},
		{ path: "sections/hero.liquid", contents: "Hero" },
	]);
	assert.equal(
		analysis.issues.some((issue) => issue.code === "THEME_JSON_PARSE_ERROR"),
		false,
	);
	assert.equal(analysis.ir.sectionInstances.length, 1);
});

test("theme analysis reports duplicate, ambiguous, and broken schema inputs", () => {
	assert.equal(
		hasIssue(
			[
				{ path: "./sections/main.liquid", contents: "one" },
				{ path: "sections/main.liquid", contents: "two" },
			],
			"THEME_DUPLICATE_NORMALIZED_PATH",
		),
		true,
	);

	const ambiguous = [
		{ path: "sections/main.liquid", contents: `{% render 'button' %}` },
		{ path: "snippets/button.liquid", contents: "plain" },
		{ path: "components/button.nz.liquid", contents: "<button>NZ</button>" },
	];
	assert.equal(hasIssue(ambiguous, "THEME_DUPLICATE_DECLARATION"), true);
	assert.equal(hasIssue(ambiguous, "THEME_AMBIGUOUS_REFERENCE"), true);
	assert.equal(hasIssue(ambiguous, "THEME_UNRESOLVED_REFERENCE"), false);

	assert.equal(
		hasIssue(
			[
				{
					path: "sections/broken.liquid",
					contents: `{% schema %}{ not json }{% endschema %}`,
				},
			],
			"THEME_SCHEMA_JSON_INVALID",
		),
		true,
	);
});

test("theme input inference handles render arguments and Liquid scope", () => {
	assert.equal(
		hasIssue(
			[
				{ path: "sections/card.liquid", contents: `{% render 'price' %}` },
				{
					path: "snippets/price.liquid",
					contents: `{{ card_product.price }}`,
				},
			],
			"THEME_RENDER_ARGUMENT_MISSING",
		),
		true,
	);
	assert.equal(
		hasIssue(
			[
				{
					path: "sections/card.liquid",
					contents: `{% render 'price', card_product: product, unused: product %}`,
				},
				{
					path: "snippets/price.liquid",
					contents: `{{ card_product.price }}`,
				},
			],
			"THEME_RENDER_ARGUMENT_UNKNOWN",
		),
		true,
	);
	assert.equal(
		hasIssue(
			[
				{
					path: "sections/main.liquid",
					contents: `{% render 'price', card_product: product %}\n{% render 'price' %}`,
				},
				{
					path: "snippets/price.liquid",
					contents: `{{ card_product.price }}`,
				},
			],
			"THEME_RENDER_ARGUMENT_INCONSISTENT",
		),
		true,
	);

	assert.equal(
		hasIssue(
			[
				{ path: "sections/main.liquid", contents: `{% render 'price' %}` },
				{
					path: "snippets/price.liquid",
					contents: `{% if card_product %}{{ card_product.price }}{% endif %}`,
				},
			],
			"THEME_RENDER_ARGUMENT_MISSING",
		),
		false,
	);
	assert.equal(
		hasIssue(
			[
				{ path: "sections/main.liquid", contents: `{% render 'price' %}` },
				{
					path: "snippets/price.liquid",
					contents:
						"{% if card_product %}{{ card_product.price }}{% endif %}{{ card_product.title }}",
				},
			],
			"THEME_RENDER_ARGUMENT_MISSING",
		),
		true,
	);
	assert.equal(
		hasIssue(
			[
				{ path: "sections/main.liquid", contents: `{% render 'local' %}` },
				{
					path: "snippets/local.liquid",
					contents: `{% assign label = 'Hi' %}{{ label }}`,
				},
			],
			"THEME_RENDER_ARGUMENT_MISSING",
		),
		false,
	);
});

test("ambient Shopify context remains unknown rather than required", () => {
	const analysis = analyzeNazareTheme([
		{ path: "sections/main.liquid", contents: `{% render 'price' %}` },
		{ path: "snippets/price.liquid", contents: `{{ product.price }}` },
	]);
	assert.equal(
		analysis.issues.some(
			(issue) => issue.code === "THEME_RENDER_ARGUMENT_MISSING",
		),
		false,
	);
	assert.deepEqual(analysis.ir.expectedInputs, [
		{
			id: "expected-input:snippets/price.liquid:product",
			path: "snippets/price.liquid",
			name: "product",
			required: false,
			requirement: "unknown",
			origin: "ambientShopifyContext",
			propertyPaths: ["price"],
			evidenceIds: ["data-access:snippets/price.liquid:product.price:1:4-1:17"],
		},
	]);
});

test("render aliases preserve input and Shopify property provenance", () => {
	const analysis = analyzeNazareTheme([
		{
			path: "sections/main.liquid",
			contents: `{% render 'price' with product as card_product %}`,
		},
		{
			path: "snippets/price.liquid",
			contents: `{% assign displayed = card_product %}{{ displayed.price }}`,
		},
	]);
	assert.equal(
		analysis.issues.some(
			(issue) =>
				issue.code === "THEME_RENDER_ARGUMENT_MISSING" ||
				issue.code === "LIQUID_UNSCANNED_SETTINGS_EXPRESSION",
		),
		false,
	);
	assert.equal(analysis.ir.renderArguments[0]?.argumentName, "card_product");
	assert.ok(
		analysis.ir.dataAccesses.some(
			(access) =>
				access.fromPath === "snippets/price.liquid" &&
				access.expression === "product.price" &&
				access.origin === "renderArgument",
		),
	);
});

